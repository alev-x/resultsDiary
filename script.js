// Получаем все кнопки табов
const tabsBtns = document.querySelectorAll(".tabs__button");
// ... (твой код для табов) ...
// Получаем все табы
const tabsItems = document.querySelectorAll(".tabs__item");

// Функции для работы с табами
function hideTabs() {
    // Скрываем все табы и убираем активность со всех кнопок
    tabsItems.forEach(item => item.classList.add("hide"));
    // Убираем класс active со всех кнопок
    tabsBtns.forEach(btn => btn.classList.remove("active"));
}
// Показываем таб по индексу и делаем кнопку активной
function showTabs(index) {
    // Показываем таб по индексу и делаем кнопку активной
    tabsItems[index].classList.remove("hide");
    // Делаем кнопку активной
    tabsBtns[index].classList.add("active");
}
// Изначально скрываем все табы и показываем первый
hideTabs();
// Показываем первый таб и делаем первую кнопку активной
showTabs(0);

// ------------------ НОВАЯ ЧАСТЬ: ФУНКЦИЯ ДЛЯ ГРАФИКА ------------------
let currentChart = null; // сюда будем сохранять текущий график, чтобы удалять его перед перерисовкой

// Функция для обновления графика на основе данных из активного таба
function updateChart() {
    // 1. Найти активную вкладку (у которой нет класса hide)
    // Создаем переменную для хранения активного таба
    let activeTab = null;
    // Проходим по всем табам и ищем тот, который не скрыт
    for (let i = 0; i < tabsItems.length; i++) {
        // Если у таба нет класса hide, значит он активный
        if (!tabsItems[i].classList.contains('hide')) {
            // Сохраняем его в переменную activeTab и выходим из цикла
            activeTab = tabsItems[i];
            // Выходим из цикла, так как нашли активный таб
            break;
        }
    }
    // Если активного таба не нашли (на всякий случай), то выходим из функции
    if (!activeTab) return;

    // 2. Внутри активной вкладки найти таблицу
    // Ищем элемент table внутри activeTab
    // Сохраняем найденную таблицу в переменную table
    const table = activeTab.querySelector('table');
    // Если таблицы нет, то выходим из функции (нет данных для графика)
    if (!table) return;

    // 3. Собрать данные из этой таблицы (твой код, только теперь table - переменная)
    // Создаем объект для хранения суммарного количества минут по каждой дате
    // Ключами будут даты, а значениями - суммарное количество минут для этой даты
    // Инициализируем пустой объект для хранения данных по дням
    const dailyData = {};
    // Проходим по всем строкам таблицы (кроме заголовка) и собираем данные
    // Получаем все строки из тела таблицы (tbody)
    // Сохраняем все строки в переменную rows
    const rows = table.querySelectorAll('tbody tr');
    // Проходим по каждой строке и извлекаем дату и количество минут
    rows.forEach(row => {
        // Извлекаем дату из второго столбца (index 1)
        // Получаем текст из ячейки с датой и обрезаем пробелы
        const date = row.cells[1]?.innerText.trim();
        // Преобразуем количество минут в число, если это возможно
        // Получаем текст из пятого столбца (index 4) с минутами и обрезаем пробелы, затем пытаемся преобразовать в число
        const minutes = parseInt(row.cells[4]?.innerText.trim(), 10);
        // Если дата есть и количество минут - это число, то добавляем его в dailyData
        if (date && !isNaN(minutes)) {
            // Если для этой даты уже есть данные, то добавляем к ним, иначе создаем новую запись
            dailyData[date] = (dailyData[date] || 0) + minutes;
        }
    });

    // 4. Подготовить метки (даты) и значения
    // Сохраняем массив дат из ключей dailyData, который будет использоваться как метки на оси X
    let labels = Object.keys(dailyData);
    // Сортировка дат (чтобы шли по порядку: 01.03.26, 02.03.26 и т.д.)
    // Сортируем метки (даты) в правильном порядке, так как они в формате "день.месяц.год"
    labels.sort((a, b) => {
        // Разбиваем даты на части (день, месяц, год) для правильной сортировки
        let [da, ma, ya] = a.split('.');
        // Разбиваем вторую дату на части
        let [db, mb, yb] = b.split('.');
        // Создаем объекты Date для сравнения (год 2000 + последние 2 цифры года, месяц -1, так как в JavaScript месяцы от 0 до 11)
        // Создаем объект Date для первой даты. В JavaScript месяцы начинаются с 0, поэтому вычитаем 1 из месяца, а год берем как 2000 + последние 2 цифры года Например, для даты "01.03.26" будет год 2026, месяц 2 (март) и день 1
        let dateA = new Date(2000 + +ya, ma-1, da);
        // Создаем объект Date для второй даты
        let dateB = new Date(2000 + +yb, mb-1, db);
        // Сравниваем даты и возвращаем результат для сортировки
        return dateA - dateB;
    });
    // Получаем массив значений минут в том же порядке, что и метки
    // Сохраняем массив значений минут, который будет использоваться для данных на графике, в том же порядке, что и метки
    // Проходим по меткам (датам) и для каждой даты берем соответствующее количество минут из dailyData, сохраняя порядок
    const dataValues = labels.map(date => dailyData[date]);

    // 5. Если данных нет, очистить график и выйти
    // Если нет меток, значит данных для графика нет
    if (labels.length === 0) {
        // Если есть текущий график, то уничтожаем его, чтобы очистить
        if (currentChart) {
            // Уничтожаем текущий график, чтобы очистить его
            currentChart.destroy();
            // Сбрасываем переменную, чтобы показать, что графика больше нет
            currentChart = null;
        }
        return;
    }

    // 6. Уничтожить старый график, если он есть
    // Если есть текущий график, то уничтожаем его, чтобы подготовиться к рисованию нового
    if (currentChart) {
        // Уничтожаем текущий график, чтобы подготовиться к рисованию нового
        currentChart.destroy();
    }

    // 7. Нарисовать новый график
    // Получаем контекст канваса для рисования графика
    const ctx = document.getElementById('timeChart').getContext('2d');
    // Создаем новый график и сохраняем его в переменную currentChart, чтобы потом можно было уничтожить при обновлении
    currentChart = new Chart(ctx, {
        // Тип графика - линия
        type: 'line',
        // Данные для графика - метки (даты) и значения (минуты)
        data: {
            // Метки для оси X - это даты
            labels: labels,
            // Датасеты - это массив объектов, каждый из которых описывает одну линию на графике
            datasets: [{
                // Название линии на графике
                label: 'Затрачено минут в день',
                // Данные для этой линии - это количество минут для каждой даты
                data: dataValues,
                // Цвет линии и заливки
                borderColor: 'rgb(59, 130, 246)',
                // Цвет заливки под линией (с прозрачностью)
                backgroundColor: 'rgba(59, 130, 246, 0.1)',
                // Кривая линия (без острых углов)
                tension: 0.1,
                // Заливка под линией
                fill: true
            }]
        },
        // Настройки графика
        options: {
            // Делаем график адаптивным, чтобы он подстраивался под размер окна
            responsive: true,
            // Настройки осей
            scales: {
                // Ось Y - это количество минут, начинаем с нуля и добавляем название оси
                y: { beginAtZero: true, title: { display: true, text: 'Минуты' } },
                // Ось X - это даты, добавляем название оси
                x: { title: { display: true, text: 'Дата' } }
            }
        }
    });
}

// ------------------ ДОБАВЛЯЕМ ВЫЗОВ ОБНОВЛЕНИЯ ГРАФИКА ПРИ КЛИКЕ НА ТАБ ------------------
// Добавляем обработчик клика на каждую кнопку таба, чтобы при переключении табов обновлять график
tabsBtns.forEach((btn, index) => btn.addEventListener("click", () => {
    // При клике на кнопку таба сначала скрываем все табы, затем показываем нужный и обновляем график
    hideTabs();
    // Показываем таб по индексу и делаем кнопку активной
    showTabs(index);
    // После переключения табов обновляем график, чтобы он отображал данные из нового активного таба
    updateChart(); // <--- вот это добавили
}));

// И при загрузке страницы тоже рисуем график
updateChart();